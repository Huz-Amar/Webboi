
public class PreviousMedicalCondition {

	public int c_id;
	public String condition_name;
	public String condition_type;
	public String date_of_diagnosis;
	public String cause;
	
	public PreviousMedicalCondition(int a, String b, String c, String d, String e)
	{
		c_id=a;
		condition_name=b;
		condition_type=c;
		date_of_diagnosis=d;
		cause=e;
	}
	

}
