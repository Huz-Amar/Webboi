
public class Client {

	public int client_id;
	public String client_name;
	public int age;
	public String sex;
	public double weight;
	public String address;
	public int number;

	
	public Client(int a, String b, int c, String d, double e, String f, int g)
	{
		client_id=a;
		client_name=b;
		age=c;
		sex=d;
		weight=e;
		address=f;
		number=g;
	}
	

}
