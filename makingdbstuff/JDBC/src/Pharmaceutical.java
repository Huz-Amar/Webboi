
public class Pharmaceutical {

	public int stock_id;
	public String name;
	public String expiration_date;
	public String sale_price;
	public String description;
	public String dosage;
	
	public Pharmaceutical(int a, String b, String c, String d, String e, String f)
	{
		stock_id=a;
		name=b;
		expiration_date=c;
		sale_price=d;
		description=e;
		dosage=f;
	}
	

}
